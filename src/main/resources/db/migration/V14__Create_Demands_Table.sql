CREATE TABLE DEMANDS (
    DEMAND_GUID VARCHAR(36) PRIMARY KEY,
    QUANTITY DOUBLE NOT NULL,
    EXPECTED_PRICE_PER_UNIT DOUBLE NOT NULL,
    DESCRIPTION VARCHAR(2000),
    STATUS VARCHAR(20) NOT NULL DEFAULT 'OPEN',
    ACTIVE BOOLEAN DEFAULT TRUE,
    CATEGORY_GUID VARCHAR(36) NOT NULL,
    SUB_CATEGORY_GUID VARCHAR(36) NOT NULL,
    BUYER_GUID VARCHAR(36) NOT NULL,
    ACCEPTED_BY_FARMER_GUID VARCHAR(36),
    FULFILLED_STOCK_GUID VARCHAR(36),
    CREATED_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CREATED_USER VARCHAR(30),
    MODIFIED_TIME TIMESTAMP NULL,
    MODIFIED_USER VARCHAR(30),
    FOREIGN KEY (CATEGORY_GUID) REFERENCES CATEGORY(CATEGORY_GUID),
    FOREIGN KEY (SUB_CATEGORY_GUID) REFERENCES SUB_CATEGORY(SUB_CATEGORY_GUID),
    FOREIGN KEY (BUYER_GUID) REFERENCES BUYERS(BUYER_GUID),
    FOREIGN KEY (ACCEPTED_BY_FARMER_GUID) REFERENCES FARMERS(FARMER_GUID),
    FOREIGN KEY (FULFILLED_STOCK_GUID) REFERENCES STOCKS(STOCK_GUID)
) ENGINE=InnoDB;
