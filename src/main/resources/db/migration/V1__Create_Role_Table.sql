CREATE TABLE ROLES (
    ROLE_GUID VARCHAR(36) PRIMARY KEY,
    ROLE_NAME VARCHAR(50) NOT NULL UNIQUE
) ENGINE=InnoDB;

CREATE TABLE USER_ROLES (
    USER_GUID VARCHAR(36) NOT NULL,
    ROLE_GUID VARCHAR(36) NOT NULL,
    PRIMARY KEY (USER_GUID, ROLE_GUID),
    FOREIGN KEY (USER_GUID) REFERENCES USERS(USER_GUID) ON DELETE CASCADE,
    FOREIGN KEY (ROLE_GUID) REFERENCES ROLES(ROLE_GUID) ON DELETE CASCADE
) ENGINE=InnoDB;

