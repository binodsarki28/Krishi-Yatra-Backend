CREATE TABLE ORDERS (
    ORDER_GUID VARCHAR(36) PRIMARY KEY,
    ORDER_QUANTITY DOUBLE NOT NULL,
    PER_UNIT_PRICE DOUBLE NOT NULL,
    TOTAL_PRICE DOUBLE NOT NULL,
    ORDER_STATUS VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    PICKUP_ADDRESS VARCHAR(1000),
    DROP_ADDRESS VARCHAR(1000),
    DELIVERY_FEE DOUBLE NULL,
    VEHICLE_TYPE VARCHAR(50) NULL,
    NOTES VARCHAR(2000) NOT NULL,
    DELIVERED_AT TIMESTAMP NULL,
    CONFLICT_MESSAGE VARCHAR(2000),
    CONFLICT_RAISED_AT TIMESTAMP NULL,
    CONFLICT_RESOLVED_AT TIMESTAMP NULL,
    BUYER_GUID VARCHAR(36) NOT NULL,
    FARMER_GUID VARCHAR(36) NOT NULL,
    STOCK_GUID VARCHAR(36) NOT NULL,
    DELIVERY_GUID VARCHAR(36),
    CREATED_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CREATED_USER VARCHAR(30),
    MODIFIED_TIME TIMESTAMP NULL,
    MODIFIED_USER VARCHAR(30),
    FOREIGN KEY (BUYER_GUID) REFERENCES BUYERS(BUYER_GUID),
    FOREIGN KEY (FARMER_GUID) REFERENCES FARMERS(FARMER_GUID),
    FOREIGN KEY (STOCK_GUID) REFERENCES STOCKS(STOCK_GUID),
    FOREIGN KEY (DELIVERY_GUID) REFERENCES DELIVERY(DELIVERY_GUID)
) ENGINE=InnoDB;
